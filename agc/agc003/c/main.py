import sys
read = sys.stdin.buffer.read

n,*a = map(int,read().split())

a_even = set(a[::2])
a.sort()

ans = 0
for i in range(1,n,2):
    ai = a[i]
    if ai in a_even:
        ans += 1

print(ans)