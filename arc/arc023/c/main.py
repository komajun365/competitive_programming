n = int(input())
a = list(map(int,input().split()))
mod = 10**9 + 7

max_n = 2010
fac, finv, inv = [0]*max_n, [0]*max_n, [0]*max_n

def comInit(max_n):
    fac[0] = fac[1] = 1
    finv[0] = finv[1] = 1
    inv[1] = 1

    for i in range(2,max_n):
      fac[i] = fac[i-1]* i% mod
      inv[i] = mod - inv[mod%i] * (mod // i) % mod
      finv[i] = finv[i-1] * inv[i] % mod

comInit(max_n)

ans = 1
before = a[0]
i = 1
cnt = 0
while i < n:
    if a[i] == -1:
        cnt += 1
    else:
        after = a[i]
        m = after - before + cnt
        for j in range(cnt):
            ans *= m-j
            ans %= mod
            ans *= inv[j+1]
            ans %= mod
        cnt = 0
        before = after
    i += 1
print(ans)
             

