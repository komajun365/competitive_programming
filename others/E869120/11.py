from collections import deque

def solve(s):
    n,*data = map(int,s.split())
    dcs = []
    it = iter(data)
    for d,c,s in zip(it,it,it):
        dcs.append([d,c,s])
    dcs.sort()
    dp = [0] * 5001
    for d,c,s in dcs:
        for j in range(d,0,-1):
            if j - c < 0:
                break
            dp[j] = max(dp[j],dp[j-c]+s)
        # print(dp[:10])
    res = max(dp)
    print(res)

