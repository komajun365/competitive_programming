n,k = map(int,input().split())
a = list(map(int,input().split()))

ans = 0
cnt = [0] * (n+1)

for ai in a:
    cnt[ai] += 1

num = k
for i in range(n+1):
    num = min(num, cnt[i])
    ans += num

print(ans)

