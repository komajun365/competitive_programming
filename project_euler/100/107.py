class UnionFind():
    def __init__(self, n):
        self.n = n
        self.parents = [-1] * n

    def find(self,x):
        if(self.parents[x] < 0):
            return x
        self.parents[x] = self.find(self.parents[x])
        return self.parents[x]

    def size(self, x):
        return self.parents[ self.find(x) ] * -1

    def same(self, x, y):
        x_root = self.find(x)
        y_root = self.find(y)
        return (x_root == y_root)

    def union(self,x,y):
        x_root = self.find(x)
        y_root = self.find(y)
        if(x_root == y_root):
            return

        if( self.parents[x_root] <= self.parents[y_root] ):
            self.parents[x_root] += self.parents[y_root]
            self.parents[y_root] = x_root
        else:
            self.parents[y_root] += self.parents[x_root]
            self.parents[x_root] = y_root

    def members(self,x):
        root = self.find(x)
        ret = [ i for i in range(self.n) if self.find(i) == root ]
        return ret

    def roots(self):
        ret = [ i for i in range(self.n) if self.parents[i] < 0]
        return ret

    def group_count(self):
        return len(self.roots())

    def all_group_members(self):
        return {r: self.members(r) for r in self.roots()}

n = 40
inf = 10**10
s = [list(map(lambda x:inf if x=='-' else int(x) ,input().split(','))) for _ in range(n)]

tot = 0
edges = []
for i in range(n):
    for j in range(i+1,n):
        if(s[i][j] != inf):
            edges.append((s[i][j],i,j))
            tot += s[i][j]

edges.sort()

ans = 0
uf = UnionFind(n)

for cost,i,j in edges:
    if(uf.same(i,j)):
        continue

    ans += cost
    uf.union(i,j)

    if(uf.size(0)==n):
        break

print(tot - ans)

